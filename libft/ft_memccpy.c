#include "libft.h"

void	*ft_memccpy(void *dest, const void *src, int c, size_t n)
{
	size_t			i;
	unsigned char	*d;
	unsigned char	*s;

	i = 0;
	d = (unsigned char *)dest;
	s = (unsigned char *)src;
	while (i++ < n)
	{
		*d = *s;
		if (*d == (unsigned char)c)
			return (++d);
		d++;
		s++;
	}
	return (0);
}
